import React, { useState } from 'react'

const BasicHook = () => {
  const[count, setCount] = useState(0)

  const increment =()=>{
    setCount(count+1)
  }
  const decrement =()=>{
    setCount(count-1)
  }
  return (
    <>
    <div  className='mt-3'>BasicHook</div>
    <div className='mt-4'>
    <button  className="bg-primary me-3 text-light" onClick={increment}>incrementfunc</button>
    <button  className="bg-secondary text-light"  onClick={decrement}>decrementfunc</button>
    </div>
    </>
  )
}

export default BasicHook


